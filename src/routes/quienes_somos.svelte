  <h1>Quienes Somos</h1>

  <section>
    <h2>Acerca de nosotros</h2>
    <p>
      Somos una organización sin fines de lucro, que nace como la respuesta
      concreta de su directiva, frente a la necesidad de poner en valor el
      cuerpo humano, potenciando el entendimiento integral de éste. Consideramos
      de suma importancia ampliar y profundizar el debate reflexivo en temáticas
      en torno al cuerpo humano y, la toma de conciencia de éste como el primer
      territorio habitable.
    </p>
  </section>
  <section>
    <h2>Misión</h2>
    <p>
      Fundación Soy Cuerpo promueve a nivel nacional el desarrollo del ser
      humano a través del cuerpo físico en áreas sociales, formativas,
      artísticas y patrimoniales, desarrollando proyectos específicos de puesta
      en valor del cuerpo.
    </p>
  </section>
  <section>
    <h2>Visión</h2>
    <p>
      Creemos que al re-posicionar las prácticas corporales como un camino
      válido, viable y fértil, podemos construir un íntegro, equitativo y
      sostenible desarrollo como sociedad y, esencialmente como seres humanos.
    </p>
  </section>
